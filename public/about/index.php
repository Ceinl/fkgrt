				<p style="text-align: justify;">
					 З 2001 року технікум підпорядковано &nbsp;Міністерству освіти і науки, молоді та спорту України.&nbsp; Технікум є базовим вищим навчальним закладом 1-2 рівнів акредитації з підготовки Державних стандартів освіти напряму «Геологія». Адміністрація та викладачі технікуму&nbsp; беруть активну участь у діяльності Ради директорів та міських методичних об’єднань вищих навчальних закладів 1-2-го рівнів акредитації.&nbsp;
				</p>
				<p style="text-align: justify;">
					 У 2005 році колектив технікуму за вагомі досягнення у вихованні молодого покоління, підготовці кадрів для геологічної галузі нагороджено Почесною грамотою Кабінету Міністрів України.
				</p>
				<p style="text-align: justify;">
					 У 2010 році за заслуги у підготовці фахівців для &nbsp;розвідки надр України Державна геологічна служба та Центральний комітет профспілки працівників геології, геодезії та картографії нагородили технікум відомчою відзнакою «Медаль В.І. Лучицького».
				</p>
				<p style="text-align: center;">
 <b>З 2012 до сьогодення</b>
				</p>
				<p style="text-align: justify;">
					 З жовтня 2012 року технікум приєднано до Київського національного університету імені Тараса Шевченка і реорганізовано в Коледж геологорозвідувальних технологій.
				</p>
				<p>
				</p>
				<p style="text-align: justify;">
 <span style="text-align: justify;">У 2019 році коледж набуває статусу закладу фахової передвищої освіти.</span><br>
				</p>
				<p style="text-align: justify;">
					 Наказом Міністерства освіти і науки України від 19 серпня 2020 року № 1072 коледж&nbsp; перейменовано у Відокремлений структурний підрозділ «Фаховий коледж геологорозвідувальних технологій Київського національного&nbsp; університету імені Тараса Шевченка».&nbsp;
				</p>
				<p style="text-align: justify;">
					 За більш як 95 років історії змінювалась не тільки назва закладу а змінювались назви спеціальностей. Змінювались покоління і цінності. Приходили нові студенти і нові працівники - обранці геологічної професії, які засвоювали всі теоретичні знання і практичні&nbsp; уміння, та виходили &nbsp;&nbsp;на простори Батьківщини в пошуках родовищ корисних копалин.
				</p>
				<p style="text-align: justify;">
					 Оскільки геологія&nbsp; - це комплексна наука, яка потребує глибоких всебічних знань, коледж випускає техніків-геофізиків, техніків-гідрогеологів, техніків-екологів, техніків з буріння, техніків з експлуатації та ремонту устаткування, механіків та слюсарів з ремонту автомобілів, фахових молодших бакалаврів з геологічного туризму.
				</p>
				<p style="text-align: justify;">
					 Адже пошук і розвідка родовищ корисних копалин неможлива на сучасному рівні без знань геофізики та комп’ютерної обробки даних. Так само, як і знання гідрогеології та екології конче необхідні при розвідці та видобуванні корисних копалин.
				</p>
				<p style="text-align: justify;">
					 Тобто, всі професії, які надає наш коледж, пов’язані з геологічною галуззю народного господарства.
