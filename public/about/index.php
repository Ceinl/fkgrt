					 У 1992 році &nbsp;&nbsp;відкрито філіал технікуму в місті Ташауз Туркменістану. Організовано набір на денне навчання за спеціальностями: «Геологія, пошуки та розвідка родовищ корисних копалин»; «Гідрогеологія та інженерна геологія»;&nbsp; «Технологія та техніка розвідки родовищ корисних копалин». Викладачами технікуму надано навчально- методичну допомогу в організації навчального процесу.
				</p>
				<p style="text-align: justify;">
					 У 1994 році стартує перепідготовка кадрів&nbsp; із числа молодших спеціалістів відповідно до наказу Міністерства освіти і науки України «Про організацію спеціального відділення перепідготовки кадрів в Київському геологорозвідувальному технікумі».
				</p>
				<p style="text-align: justify;">
					 Продовжується традиція фахової &nbsp;методичної роботи. Викладачі коледжу – автори більше 60 підручників і навчальних посібників, виданих у різний час; серед них Анохіна І.Д., Башлик С.М., Брацлавський Й.А., Бревда Н.Й., Горячко І.В., Заворотько Ю.М., Загибайло Г.Т., Заєць О.П., Камзіст Ж.С., Коваленко А.В., Коротких І.В., Кошелєв І.М. Левітес Я.М., Лузан В.В., Люкшин В.С., Нагля В.В., Овчинніков Л.І., Петелько О.Ф., Рублевська Л.В., Сапфіров Г.М., Фролов О.Ф., Гонтарев Ю.Ф. та інші.
