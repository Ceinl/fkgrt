				<p style="text-align: justify;">
					 Технікум відкрив багатьом вихованцям шлях у науку, на найвищі щаблі державного і господарського управління.&nbsp; Серед випускників технікуму – народні депутати України, керівники галузей, великих підприємств, наукових установ, закладів вищої освіти.
				</p>
				<p style="text-align: justify;">
					 З 1991 року у технікумі &nbsp;багато уваги приділяється національному відродженню та розширенню &nbsp;освітньої &nbsp;діяльності &nbsp;а саме:
				</p>
				<p style="text-align: justify;">
					 -відкриття&nbsp; нових спеціальностей як «Обробка геофізичної інформації на ЕОМ», «Діловодство», «Економіка&nbsp; і планування геологорозвідувальних робіт», «Бухгалтерський облік і аудит», «Обслуговування та ремонт автомобілів та двигунів», «Технічне обслуговування і ремонт електронно-обчислювальної техніки в геофізичній апаратурі», &nbsp;«Охорона навколишнього середовища&nbsp; при геологорозвідувальних роботах&nbsp; і раціональне використання природних ресурсів»;
				</p>
				<p style="text-align: justify;">
					 - відкриття підготовки іноземних громадян до вступу у навчальні заклади на контрактній основі серед професійно-технічних закладів одного із перших в Україні.
				</p>
				<p style="text-align: justify;">
					 Розширюється співпраця з організаціями геологічної галузі як в Україні, так і в інших країнах. Ефективна співпраця з Південно-Аральським гідрогеологічним &nbsp;трестом&nbsp; ВО «Туркменгеологія».&nbsp;&nbsp;Викладачі та студенти технікуму&nbsp; провели&nbsp; геолого-гідрогеологічну зйомку геофізичними методами для виявлення прісноводних лінз&nbsp; у Північному Каракумі (1990-1993).
				</p>
				<p class="img-wrap mr-auto ml-auto">
 <img src="/upload/medialibrary/f8f/index-img.png">
