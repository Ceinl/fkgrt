				<p style="text-align: justify;">
					 Викладачі технікуму у різні роки надавали допомогу у пошуках і розвідці родовищ корисних копалин і підготовці фахівців геологічного профілю у Алжирі, В’єтнамі, Китаї, Лаосі, Ірані, Нігерії, Ефіопії та інших країнах. Протягом багатьох років у технікумі проходили стажування викладачі технікумів геологічного профілю з країн, що розвивалися.<br>
				</p>
				<p class="img-wrap mr-auto ml-auto">
 <img src="/about/img0003.jpg"> <!--вьетнам-->
				</p>
				<p style="text-align: justify;">
					 Київський геологорозвідувальний технікум був підпорядкований Міністерству геології Радянського Союзу, згодом передпорядкований &nbsp;Міністерству геології України.
				</p>
				<p style="text-align: justify;">
					 Будівля навчального корпусу коледжу є одним із еталонів повоєнної архітектури. А сходи, що ведуть до нього, досі користуються популярністю серед кінематографістів. Кадр з фільму <b>"Королева бензоколонки</b>". Зйомка велась на сходах геологорозвідувального коледжу.<br>
