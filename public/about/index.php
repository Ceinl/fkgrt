					 Багато випускників пам’ятають тих, хто був на початку існування коледжу як покоління високо інтелігентних, досвідчених&nbsp;викладачів, які віддавали знання та&nbsp;душу своїй справі.
				</p>
				<p style="text-align: justify;">
					 У технікумі відбувся процес “зміни поколінь”. На зміну заслуженим викладачам поступово приходить молодь, переймаючи традиції та досвід своїх попередників.
				</p>
