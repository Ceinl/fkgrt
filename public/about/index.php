 <span type="#_x0000_t75" alt="http://geophys.knu.ua/images/training/bak3/2019_bak3_02lg.jpg" style="width: 481.5pt; height: 257.25pt;"> <span src="file:///C:/Users/Svetlana/AppData/Local/Temp/msohtmlclip1/08/clip_image007.jpg"> </span></span>
