 <img src="/about/img001.jpg">
				</p>
				<p style="text-align: justify;">
					 1 вересня 1986 року відкрито геологічний музей технікуму. Фонди музею нараховують понад три тисячі зразків мінералів і гірських порід із родовищ різних регіонів України та світу і більше 300 одиниць палеонтологічної колекції. В музеї проводяться навчальні заняття з мінералогії, петрографії, корисних копалин, загальної та історичної геології., а також екскурсії для студентів, школярів.
				</p>
				<p class="img-wrap mr-auto ml-auto">
 <img src="/about/mimage002.jpg">
				</p>
				<p style="text-align: justify;">
				</p>
				<p class="resp-tabs-container text-sm-left tabs-lg-collapsed" style="text-align: justify;">
				</p>
				<p style="text-align: justify;">
				</p>
				<p style="text-align: justify;">
 <span type="#_x0000_t75" alt="http://kgrt.univ.kiev.ua/museum/m1.jpg" style="width: 466.5pt; height: 349.5pt;"> <span src="file:///C:/Users/Svetlana/AppData/Local/Temp/msohtmlclip1/08/clip_image011.jpg"> </span></span>
				</p>
				<p style="text-align: justify;">
					 З 1987 року в технікумі діє спеціальне відділення перепідготовки кадрів середньої&nbsp;&nbsp; кваліфікації з відривом від виробництва для навчання експлуатації, обслуговуванню і ремонту складної геофізичної апаратури. Також&nbsp; проходять стажування викладачі технікумів геологічного профілю з Лаосу, МНР, В’єтнаму.&nbsp;
				</p>
				<p style="text-align: justify;">
					 Успішно реалізуються договори про співдружність з В’єтнамським політехнікумом, північним та південним&nbsp; &nbsp;геологорозвідувальним&nbsp; технікумами з ДРВ, геологічною школою&nbsp; Заводовою&nbsp; &nbsp;м. Вроцлава (Польща).
				</p>
