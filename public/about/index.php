				</p>
				<p align="center">
 <span type="#_x0000_t75" style="width: 422.25pt; height: 304.5pt;"> <span src="file:///C:/Users/Svetlana/AppData/Local/Temp/msohtmlclip1/08/clip_image003.jpg"> </span></span>
