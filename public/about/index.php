				</p>
				<p style="text-align: justify;">
				</p>
				<p style="text-align: justify;">
					 З 1957 року розпочалася підготовка фахівців для країн, що стали на шлях самостійного розвитку, звільнившись від колоніалізму. На той час у Києві ми були першими серед професійно-технічних закладів, де почали навчатись іноземні студенти. Технікум підготував близько 500 фахівців для країн Азії, Африки і Латинської Америки.
				</p>
				<p class="img-wrap mr-auto ml-auto">
 <img src="/upload/medialibrary/a95/index-img.png">
				</p>
				<p style="text-align: justify;">
 <span style="text-align: justify;">У 1967 році було відкрито навчальний полігон у Житомирській області для проведення навчальних практик з топографії, бурової і гірничої справи, геофізичних, гідрогеологічних та інженерно-геологічних досліджень.</span><br>
