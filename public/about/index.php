							 За освітньо-професійною програмою&nbsp;“<b><span style="color: #9d0a0f;">Обслуговування та ремонт автомобілів і двигунів”</span></b>&nbsp;коледж готує фахових молодших бакалаврів для виробничо-технічної та організаційної діяльності, пов’язаної з технічним обслуговуванням автомобілів і двигунів. Випускникам присвоюється&nbsp; освітня кваліфікація фахового молодшого бакалавра з автомобільного транспорту.&nbsp;
						</p>
						<p align="justify">
 <br>
						</p>
					</div>
				</div>
