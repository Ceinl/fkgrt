				</p>
				<div class="offset-top-30">
				</div>
				<h2 class="text-bold veil reveal-lg-block">Історія коледжу</h2>
				<p class="resp-tabs-container text-sm-left tabs-lg-collapsed">
				</p>
				<p style="text-align: center;">
 <b><span style="background: #ffffff;">1930-1941</span></b>
				</p>
				<p style="text-align: justify;">
 <span style="background-color: white; text-align: justify;">31 липня 1930 року засновано Київський геологорозвідувальний технікум (КГРТ) наказом Головного геологорозвідувального управління Вищої Ради народного господарства СРСР на базі топографічної професійної школи.</span>
				</p>
				<p style="background: white;">
				</p>
				<p style="text-align: justify;">
					 Технікум розпочав підготовку фахівців для геологічної галузі з чотирьох спеціальностей: <i>геологорозвівальної, гідрогеологічної, бурової,</i> а також <i>топографічної.</i>
				</p>
				<p style="background: white;">
				</p>
				<p>
				</p>
				<p style="background: white; text-align: justify;">
					 КГРТ був розташований по вул. Жертв революції, 12 (нині Михайлівська), і займав один поверх будівлі. У цьому ж будинку знаходилися ще чотири навчальні заклади – два інститути і два технікуми. Заняття проводилися у дві зміни, починаючи з 16 години. У технікумі навчалося близько 150 учнів. На початку не було ніякої навчальної бази, за винятком невеликої бібліотеки. Не було й штатних викладачів. Відбувалося становлення навчального закладу: створювалися навчальні кабінети, поповнювалася літературою бібліотека, формувався педагогічний колектив.
				</p>
				<p style="background: white; text-align: justify;">
					 У 1934 році відбувся перший випуск фахівців геологічної галузі. У цьому ж році будинок по вул. Жертв революції, 12, де був розташований технікум, передано Інституту Червоної професури, переведеному із Харкова до столиці УРСР. Колективом технікуму &nbsp;докладено &nbsp;багато зусиль для права на переїзд технікуму в нове приміщення Індустріального робфаку по вул. Червоноармійській, 23 (нині Велика Васильківська), де було зайнято 6 кімнат цокольного поверху. Це сприяло розпочати 1934-35 навчальний рік з шістьма навчальними групами першого курсу нового набору.
				</p>
				<p style="background: white; text-align: justify;">
					 Через деякий час аудиторний фонд технікуму поповнився ще чотирма аудиторіями, що дало змогу проводити заняття у дві зміни з повним складом учнів.
				</p>
				<p style="background: white; text-align: justify;">
					 Технікум було перейменовано в <i>геолого-гідрогеодезичний</i>, у результаті чого з’явилися нові спеціальності: <i>маркшейдерська і авіафототопографічна</i>. Кількість учнів була приблизно 500 осіб. Кількість штатних викладачів складала близько 20 осіб. Були створені <i>фізичний, геодезичний, мінералогічний</i> кабінети.
				</p>
				<p style="background: white; text-align: justify;">
				</p>
				<p style="text-align: justify;">
				</p>
				<p style="background: white; text-align: justify;">
					 У дворі побудовано бурову вишку, що сприяло проведенню практик із ручного буріння. Бібліотека технікуму була розташована на бульварі Тараса Шевченка, 4, у приміщенні геологічного тресту.
				</p>
				<p style="background: white; text-align: justify;">
					 З кожним роком справи Київського геологорозвідувального технікуму йшли вгору. Випуск молодих спеціалістів щорічно складав близько 100 осіб, а кількість учнів поповнювалася за рахунок нового набору (5-6 навчальних груп) з осіб, що закінчили семирічну школу. Вже в ті часи технікум набув велику популярність в Україні та інших союзних республіках. Кількість вступників на геологічні спеціальності з кожним роком зростала. Учні проходили практику на усій території Радянського Союзу, а після закінчення закладу працювали у різних куточках країни.&nbsp;
				</p>
				<p style="background: white; text-align: justify;">
					 За період з 1934 до 1941 року технікум випустив понад 1000 фахівців.
				</p>
				<p style="background: white; text-align: center;">
 <b><span style="background: #ffffff;">1941- 1942</span></b>
				</p>
				<p style="background: white; text-align: justify;">
					 На початку Другої світової війни значна частина викладачів і учнів технікуму пішли на фронт. КГРТ було евакуйовано до м. Семипалатинськ (Казахстан), де продовжувалася підготовка фахівців для геологічної галузі країни, й донині функціонує геологорозвідувальний технікум, створений на базі Київського.
				</p>
				<p style="text-align: center;">
 <b><span style="background: #ffffff;">1943-1948</span></b>
				</p>
				<p>
				</p>
				<p style="text-align: justify;">
				</p>
				<p style="text-align: justify;">
					 Після звільнення міста Києва в 1943 році почалася робота з відновлення Київського геологорозвідувального технікуму. Технікум відновив свою роботу у приміщенні школи №30 м. Києва по вул. Саксаганського, 64 на правах оренди приміщень з проведенням навчальних занять у третю зміну – з 20 години. На правах оренди використовувалися і приміщення середньої школи №136 по вул. Жилянській.
				</p>
				<p style="text-align: justify;">
					 У 1945 році, після закінчення Другої світової війни, до технікуму повернулися учні, навчання яких перервала війна. Із збільшенням контингенту гостро стала проблема аудиторного фонду. Потреба в геологічних кадрах з розвитком народного господарства країни зростала. Урядом було прийнято рішення про будівництво студетського містечка Київського геологорозвідувального технікуму.&nbsp;
				</p>
				<p style="text-align: justify;">
					 У 1947 році на Черепановій горі розпочато будівництво навчального корпусу та гуртожитку.
				</p>
				<p style="text-align: center;">
 <b>1949-1968</b>
				</p>
				<p>
 <span type="#_x0000_t75" alt="858" style="width: 202.5pt; height: 142.5pt;"> <span src="file:///C:/Users/Svetlana/AppData/Local/Temp/msohtmlclip1/08/clip_image001.jpg"> <span type="square"> </span></span></span><span type="#_x0000_t75" style="width: 177pt; height: 171pt;"> <span src="file:///C:/Users/Svetlana/AppData/Local/Temp/msohtmlclip1/08/clip_image002.png"> </span></span>
