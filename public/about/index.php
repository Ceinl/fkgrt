 <b>1990-2000</b>
				</p>
				<p style="text-align: justify;">
					 За час незалежності України технікум стає базовим серед галузевих навчальних закладів 1-2-го рівнів акредитації &nbsp;та одним з кращих серед коледжів та технікумів України з підготовки молодших спеціалістів для геологічної галузі і всього виробничо-господарського комплексу України.
				</p>
				<p style="text-align: justify;">
					 Зростає матеріальна-технічна база технікуму, зміцнюється науково-методичний потенціал, зусилля педагогічного колективу спрямовуються на виконання головного завдання – підготовку висококваліфікованого, компетентного, конкурентоспроможного спеціаліста, готового до активної, творчої, професійної діяльності.
				</p>
				<p class="img-wrap mr-auto ml-auto">
 <img src="/upload/medialibrary/c85/index-img.png">
