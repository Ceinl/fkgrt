					 Багато з того покоління «шестидесятників, сімдесятників,&nbsp; вісімдесятників» &nbsp;та початку 90-х років &nbsp;- &nbsp;це заслужені люди, “золотий фонд” нашого коледжу, керівники підрозділів і викладачі, досягнення і нагороди яких важко перелічити.&nbsp;
